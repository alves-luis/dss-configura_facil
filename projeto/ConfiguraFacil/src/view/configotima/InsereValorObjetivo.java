    private void calcularButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularButtonActionPerformed
      String valueS = this.valorField.getText();
      double value = 0;
      try {
        value = Double.parseDouble(valueS);
      }
      catch (NumberFormatException e) {
      }
      if (value != 0) {
        List<Componente> comps = this.facade.getConfiguracaoOtima(value);
        facade.adicionaCompTemporario(comps);
        cardPanel.add(new ListaSugeridos(facade, parent,comps), "LISTA");
        CardLayout cl = (CardLayout) cardPanel.getLayout();
        cl.show(cardPanel, "LISTA");
      }
    }//GEN-LAST:event_calcularButtonActionPerformed


